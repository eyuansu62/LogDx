#!/usr/bin/env python3
"""Release check: assert that every committed `eval_diagnosis_*.json`
file's per-method case-ID set exactly matches its corresponding
diagnosis manifest's case-ID set.

Per Codex 2026-06-11 F1 [high]: cleanup tools that remove rows from
`results/<split>/diagnoses/<diagnoser>/<method>.jsonl` (e.g. the
2026-06-09 and 2026-06-10 provider_error cleanups) must be paired
with eval-file regeneration. Otherwise the eval file scores rows
that no longer exist in the manifest; downstream reports publish
metrics that cannot be reproduced from shipped manifests.

Exits 0 on consistent state. Exits 1 with a per-method diff
otherwise.
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent


def manifest_case_ids(manifest: Path) -> set[str]:
    out: set[str] = set()
    if not manifest.exists():
        return out
    with manifest.open(encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                row = json.loads(line)
            except json.JSONDecodeError:
                continue
            cid = row.get("case_id")
            if cid:
                out.add(cid)
    return out


def eval_cases_by_method(eval_path: Path) -> dict[str, dict[str, dict]]:
    """Return {context_method: {case_id: case_dict}}.

    Per Codex 2026-06-17 F2 [high]: the consistency check needs the
    full case payload (not just IDs) so it can verify that
    excluded-exempt eval rows actually carry zero-score abstention
    semantics, not stale inflated numbers from a hand-edit / partial
    regeneration.
    """
    out: dict[str, dict[str, dict]] = {}
    if not eval_path.exists():
        return out
    try:
        data = json.loads(eval_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError:
        return out
    methods = data.get("methods") or []
    for m in methods:
        ctx_method = m.get("context_method")
        if not ctx_method:
            continue
        cases = m.get("cases") or []
        out[ctx_method] = {
            c.get("case_id"): c for c in cases if c.get("case_id")
        }
    return out


def _excluded_case_is_zero_score(case: dict) -> tuple[bool, str]:
    """Per Codex 2026-06-17 F2 [high]: verify an exclusion-exempt
    eval row carries the synthetic marker AND zero-score abstention
    semantics. Returns (ok, reason). Reason is a human-readable
    description of the FIRST violation found, or "" when ok."""
    pe = case.get("provider_error") or ""
    if "[historical exclusion]" not in pe:
        return False, (
            f"provider_error={pe[:80]!r} does not carry the "
            f"'[historical exclusion]' marker (was the row "
            f"regenerated by evaluate_diagnosis.py after the "
            f"exclusion was added?)"
        )
    if case.get("diagnosis_success") is True:
        return False, "diagnosis_success=True but exclusion should be a failure"
    if case.get("abstained") is False:
        return False, "abstained=False but exclusion should be an abstention"
    # Numeric metrics that contribute to macro means must be 0 (or
    # None) — anything else means a stale/edited row leaked inflated
    # numbers into the eval. Some metrics are nullable when GT is
    # absent (None is fine); non-zero numeric values are not.
    # Per Codex 2026-06-18 F1 [high]: SCORE_FIELDS now mirrors EVERY
    # per-case field that `evaluate_method` (in evaluate_diagnosis.py)
    # macro-averages — pre-fix, `required_signal_mention_recall` and
    # `category_match_score_v1_1` were omitted, so a stale row with
    # those two at 1.0 plus all other scores at 0 silently passed
    # the gate and still inflated `macro_required_signal_mention_
    # recall` / `macro_category_match_score_v1_1` in the published
    # report.
    SCORE_FIELDS = (
        "diagnosis_score_v1", "diagnosis_score_v1_1",
        "category_accuracy", "category_match_score_v1_1",
        "required_signal_mention_recall",
        "critical_signal_mention_recall",
        "must_mention_coverage",
        "relevant_file_recall", "relevant_test_recall",
        "valid_evidence_quote_rate",
    )
    for f in SCORE_FIELDS:
        v = case.get(f)
        if v is None or v == 0 or v == 0.0:
            continue
        return False, (
            f"score field {f}={v!r} is non-zero on an excluded row "
            f"(expected 0.0 or null)"
        )
    # Per Codex 2026-06-18 F1 [high]: boolean / list fields that
    # contribute to per-method rates (forbidden_claim_violations →
    # forbidden_rate, confident_error → confident_error_rate,
    # confident_error_v1_1 → confident_error_rate_v1_1) must also be
    # "no-violation" on a properly synthesized excluded row. A stale
    # row with `confident_error=True` would inflate the rate's
    # numerator even though every numeric field was 0.
    forbidden = case.get("forbidden_claim_violations")
    if forbidden:
        return False, (
            f"forbidden_claim_violations={forbidden!r} non-empty on "
            f"excluded row (expected [])"
        )
    if case.get("confident_error") is True:
        return False, "confident_error=True on excluded row (expected False)"
    if case.get("confident_error_v1_1") is True:
        return False, (
            "confident_error_v1_1=True on excluded row (expected False)"
        )
    return True, ""


def _load_exclusion_set(path: Path) -> set[tuple[str, str, str, str]]:
    """Per Codex 2026-06-15 F1 [high]: synthesized excluded rows
    appear in eval files (as zero-score abstentions injected by
    evaluate_diagnosis.py) but NOT in diagnosis manifests. The
    consistency check needs to know about them so the synthesized
    rows don't get flagged as "in eval but not manifest"."""
    if not path.exists():
        return set()
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError:
        return set()
    return {
        (e["split"], e["diagnoser"], e["method"], e["case_id"])
        for e in (data.get("exclusions") or [])
    }


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--results-dir", type=Path, default=REPO / "results")
    ap.add_argument(
        "--exclusions",
        type=Path,
        default=REPO / "configs" / "historical_provider_error_exclusions.json",
        help="Historical-exclusion manifest; synthesized eval rows for these "
             "(split, diagnoser, method, case_id) tuples are not required to "
             "appear in diagnosis manifests.",
    )
    args = ap.parse_args()
    exclusion_set = _load_exclusion_set(args.exclusions)

    failures: list[str] = []
    # Walk recursively for every eval_diagnosis_*.json.
    for eval_path in sorted(args.results_dir.rglob("eval_diagnosis_*.json")):
        # Locate the diagnosis dir alongside the eval file:
        # results/<split>/eval_diagnosis_<diagnoser>.json
        # results/<split>/diagnoses/<diagnoser>/<method>.jsonl
        split_dir = eval_path.parent
        diagnoser = eval_path.stem.removeprefix("eval_diagnosis_")
        diag_root = split_dir / "diagnoses" / diagnoser
        if not diag_root.exists():
            # eval file with no corresponding diagnoses dir → orphaned
            failures.append(
                f"{eval_path.relative_to(args.results_dir)}: orphaned "
                f"(no {diag_root.relative_to(args.results_dir)})"
            )
            continue
        # The eval path is `results/<split>/eval_diagnosis_<diag>.json`;
        # extract <split> for exclusion-set lookups.
        split_label = str(split_dir.relative_to(args.results_dir))
        eval_methods_full = eval_cases_by_method(eval_path)
        eval_methods = {
            m: set(cases.keys()) for m, cases in eval_methods_full.items()
        }
        # Per Codex 2026-06-12 F2 [medium]: also assert the SET of
        # methods matches between eval file and manifest directory.
        # Pre-fix, the check only iterated over methods present in the
        # eval file; a manifest method missing from a stale eval file
        # was silently ignored. Now we compare both directions.
        manifest_method_set = {
            m.stem for m in diag_root.glob("*.jsonl")
        }
        eval_method_set = set(eval_methods.keys())
        only_in_eval = eval_method_set - manifest_method_set
        only_in_manifest = manifest_method_set - eval_method_set
        for m in sorted(only_in_eval):
            failures.append(
                f"{eval_path.relative_to(args.results_dir)} "
                f"method={m!r}: in eval but no manifest "
                f"{diag_root.relative_to(args.results_dir)}/{m}.jsonl"
            )
        for m in sorted(only_in_manifest):
            failures.append(
                f"{eval_path.relative_to(args.results_dir)} "
                f"method={m!r}: manifest exists but eval omits this method"
            )
        for method, eval_set in eval_methods.items():
            if method not in manifest_method_set:
                # Already reported above; skip per-case detail.
                continue
            manifest = diag_root / f"{method}.jsonl"
            manifest_set = manifest_case_ids(manifest)
            if eval_set != manifest_set:
                missing_in_manifest_raw = eval_set - manifest_set
                # Per Codex 2026-06-15 F1 [high]: cases in the
                # exclusion list are intentionally injected by
                # evaluate_diagnosis.py as zero-score abstentions
                # and don't have a corresponding manifest row.
                # Per Codex 2026-06-17 F2 [high]: AND verify the
                # eval row for each exempted case actually has
                # zero-score abstention semantics — a stale or
                # hand-edited row keeping nonzero metrics must NOT
                # be exempted.
                missing_in_manifest: set[str] = set()
                for cid in missing_in_manifest_raw:
                    if (split_label, diagnoser, method, cid) not in exclusion_set:
                        missing_in_manifest.add(cid)
                        continue
                    eval_row = eval_methods_full.get(method, {}).get(cid)
                    if eval_row is None:
                        missing_in_manifest.add(cid)
                        continue
                    ok, reason = _excluded_case_is_zero_score(eval_row)
                    if not ok:
                        failures.append(
                            f"{eval_path.relative_to(args.results_dir)} "
                            f"method={method!r} case={cid!r}: "
                            f"exclusion-exempt row has stale/inflated "
                            f"metrics ({reason})"
                        )
                missing_in_eval = manifest_set - eval_set
                if not missing_in_manifest and not missing_in_eval:
                    continue
                detail = []
                if missing_in_manifest:
                    detail.append(
                        f"in eval but not manifest (and not in exclusions): "
                        f"{sorted(missing_in_manifest)}"
                    )
                if missing_in_eval:
                    detail.append(
                        f"in manifest but not eval: "
                        f"{sorted(missing_in_eval)}"
                    )
                if detail:
                    failures.append(
                        f"{eval_path.relative_to(args.results_dir)} "
                        f"method={method!r}: {'; '.join(detail)}"
                    )

    if failures:
        print(
            "FAIL: eval-vs-manifest case-ID mismatches:", file=sys.stderr
        )
        for f in failures:
            print(f"  {f}", file=sys.stderr)
        print(
            f"\nTotal mismatches: {len(failures)}. Regenerate the affected "
            f"eval files with `python3 tools/evaluate_diagnosis.py "
            f"--split <split> --diagnoser <diagnoser>` after any manifest "
            f"row removal/cleanup.",
            file=sys.stderr,
        )
        return 1

    print(
        f"OK: every eval_diagnosis_*.json case-ID set matches its "
        f"corresponding manifest in {args.results_dir}."
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
